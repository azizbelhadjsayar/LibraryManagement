    private void booksTABLEMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_booksTABLEMouseClicked
        DefaultTableModel model = (DefaultTableModel) booksTABLE.getModel();
        int selectIndex = booksTABLE.getSelectedRow();
        EnableDisableForm(true);
        authorInput.removeAllItems();
        loadAuthors();
        AuthorService as = new AuthorService();
        Author a = as.getAuthorbyFullname(model.getValueAt(selectIndex, 2).toString());
        isbnInput.setText(model.getValueAt(selectIndex, 0).toString());
        titleInput.setText(model.getValueAt(selectIndex, 1).toString());
        authorInput.setSelectedItem(a.getId()+"."+model.getValueAt(selectIndex, 2).toString());
        languageInput.setText(model.getValueAt(selectIndex, 3).toString());
        pagesInput.setText(model.getValueAt(selectIndex, 4).toString());
        priceInput.setText(model.getValueAt(selectIndex, 5).toString());

    }//GEN-LAST:event_booksTABLEMouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int isbn = Integer.parseInt(isbnInput.getText());
        String title = titleInput.getText();
        String author = (String)authorInput.getSelectedItem();       
        int author_id = Integer.parseInt(author.split("\\.")[0]);
        String language = languageInput.getText();
        int pages = Integer.parseInt(pagesInput.getText());
        double price = Double.parseDouble(priceInput.getText());
        BookService BS = new BookService();
        BS.editBook(isbn, title, author_id, language, pages, price);
        Books obj = new Books();
        this.setVisible(false);
        obj.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

