    private void loadBooks() {
        DefaultTableModel model = (DefaultTableModel)booksTABLE.getModel();
        model.setRowCount(0);
        try{
            Connection connection = BibliothequeDAO.getConnection();
            java.sql.Statement statement = connection.createStatement();
            String query = "select b.isbn, b.title, a.fullname, b.language, b.numberofpages, b.price from book b join author a on b.author_id = a.id;";
            ResultSet rs = statement.executeQuery(query);
            while(rs.next()){
                Object rowData[] = new Object[6];
                rowData[0] = rs.getInt(1);
                rowData[1] = rs.getString(2);
                rowData[2] = rs.getString(3);
                rowData[3] = rs.getString(4);
                rowData[4] = rs.getInt(5);
                rowData[5] = rs.getDouble(6);
                model.addRow(rowData);
            }
        }
        catch(SQLException e){
            e.printStackTrace();
        }
    }
    
    public void loadAuthors() {
        AuthorService aus = new AuthorService();
        ArrayList <Author> authors = aus.getAllAuthors();
        String[] items = {};
        for(int i=0; i<authors.size();i++) {
            String item = authors.get(i).getId()+"."+authors.get(i).getFullname();
            authorInput.addItem(item);
        }
    }

